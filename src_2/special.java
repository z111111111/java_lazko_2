public class special extends Car {

    protected boolean migalka;

    public special(String color, double maxVelocity, boolean gear, double nowVelocity, double price, boolean migalka) {
        this.color = color;
        this.maxVelocity = maxVelocity;
        this.gear = gear;
        this.nowVelocity = nowVelocity;
        this.price = price;
        this.migalka = migalka;
    }
}